#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2)
{
	size_t j;
	unsigned char *source1;
	unsigned char *source2;

	source1 = (unsigned char *) s1;
	source2 = (unsigned char *) s2;
	return (ft_strcmp(source1, source2));
}

int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}