#include "libft.h"

char *ft_strchr(const char *a, int b)
{
	int i;
	char *f;

	i = 0;
	while (a[i] != '\0')
	{
		if(a[i] == b)
		{
			f = a[i];
			return (f);
		}
		i++;
	}
	return (NULL);
}